/**
 * The operation.
 */
export enum Operation {
    /**
     * The equal operation.
     */
    Eq = "eq",
    /**
     * The greater than operation.
     */
    Gt = "gt",
    /**
     * The greater than or equal to operation.
     */
    Gte = "gte",
    /**
     * The less than operation.
     */
    Lt = "lt",
    /**
     * The less than or equal to operation.
     */
    Lte = "lte",
    /**
     * The not equal operation.
     */
    Ne = "ne",
}